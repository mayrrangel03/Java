package exercicios;

public class Pessoa {
	private String nome;
	private int idade;
	private int Novaidade;

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getNovaidade() {
		return Novaidade;
	}

	public void setNovaidade(int novaidade) {
		Novaidade = novaidade;
	}
	

}
